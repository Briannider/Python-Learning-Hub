languages = ["Python", "Ruby", "PHP", "C++", "C", "Javascript", "Java"]

# Print numbers from 0 to 5
i = 0
while i <= 5:
    print(i)  # Print the current number
    i += 1

# Print '#' symbols based on the number
i = 0
while i <= 5:
    print(i * " # ")  # Print '#' repeated based on the number
    i += 1

# Print elements from the list
i = 0
while i < len(languages):
    print(languages[i])  # Print the element at the current index
    i += 1
