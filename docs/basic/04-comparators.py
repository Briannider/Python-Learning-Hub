"""
Comparators in Python

This script shows how to use comparators in Python to compare two numbers.
"""

# Define two numbers
number1 = 10
number2 = 10


# Check if the numbers are equal
if number1 == number2:
    print("The numbers are equal")
# Check if the first number is greater than the second
elif number1 > number2:
    print("The first number is greater than the second")
# Check if the first number is less than the second
else:
    print("The first number is less than the second")


# Print examples of comparators
print(2 == 2)  # Equal
print(2 <= 2)  # Less or equal
print(2 >= 2)  # Greater or equal
print(2 != 2)  # Not equal
