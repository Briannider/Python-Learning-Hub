"""
Variables in Python

In this example, we will see how to declare variables in Python.

Variables are used to store values in a program. They have a name, and you can assign a value to them.

In this example, we will declare 5 variables: a string, an integer, a float, a boolean and another boolean.

"""

# String
greeting = "Hello World"  # This is a string

# Integer
number = 22  # This is an integer

# Float
decimal_number = 22.5  # This is a float

# Boolean
is_false = False  # This is a boolean

# Boolean
is_true = True  # This is a boolean

# Print the variables
print(greeting)
print(number)
print(decimal_number)
print(is_false)
print(is_true)
