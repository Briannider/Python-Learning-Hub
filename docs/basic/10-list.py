# This script demonstrates the use of list indexing and slicing
languages = ["Python", "Ruby", "PHP", "Javascript", "Java"]

# Replace the fourth element with "C++"
languages[3] = "C++"

# Print the second last element
print(languages[-2])

# Print the elements from index 1 to 4
print(languages[1:4])

# Print the element at index 2
print(languages[2:3])

# Print the elements from index 1 to 3
print(languages[1:3])
