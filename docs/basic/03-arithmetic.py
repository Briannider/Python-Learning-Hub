"""
Basic Arithmetic Operations
===========================

This module contains examples of basic arithmetic operations in Python.
"""

# Sum
number1 = 10
number2 = 5
sum = number1 + number2

# Subtraction
subtraction = number1 - number2

# Multiplication
multiplication = number1 * number2

# Division
division = number1 / number2

# Modulus
modulus = number1 % number2

# Power
power = number1**number2

# Integer Division
integer_division = number1 // number2


print(sum)
print(subtraction)
print(multiplication)
print(division)
print(modulus)
print(power)
print(integer_division)
